package hk.edu.cuhk.ie.iems5722.group25.joycorner.animation;

public class WidthAndHeightOfView {
    private int width;
    private int height;

    WidthAndHeightOfView() {
    }

    void setWidth(int width) {
        this.width = width;
    }

    void setHeight(int height) {
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
